package com.zenika.bo;

public class Mapping {
	
	private String cityCode;
	private String cityLabel;
	private String metropolisCode;
	private String metropolisLabel;
	private String metropolisType;
	
	public String getCityCode() {
		return cityCode;
	}
	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}
	public String getCityLabel() {
		return cityLabel;
	}
	public void setCityLabel(String cityLabel) {
		this.cityLabel = cityLabel;
	}
	public String getMetropolisCode() {
		return metropolisCode;
	}
	public void setMetropolisCode(String metropolisCode) {
		this.metropolisCode = metropolisCode;
	}
	public String getMetropolisLabel() {
		return metropolisLabel;
	}
	public void setMetropolisLabel(String metropolisLabel) {
		this.metropolisLabel = metropolisLabel;
	}
	public String getMetropolisType() {
		return metropolisType;
	}
	public void setMetropolisType(String metropolisType) {
		this.metropolisType = metropolisType;
	}
	
	
}
